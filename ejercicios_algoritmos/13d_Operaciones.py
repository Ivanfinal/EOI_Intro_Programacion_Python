
num1 = int(input("Introduce primer numero: "))
num2 = int(input("Introduce segundo numero: "))
operacion = input("Introduce operacion: ")

if operacion == '+':
    print(num1+num2)
elif operacion == '-':
    print(num1-num2)
elif operacion == '*':
    print(num1*num2)
elif operacion == '/':
    print(num1/num2)